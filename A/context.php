      <strong>CPC</strong> - стоимость клика.<br />
      <strong>CPA</strong> - стоимость конверсии.<br />
      <strong>CTR</strong> - кликабельность. Рассматривать только в рамках блока.<br />
      <strong>CPO</strong> - стоимость привлеченного клиента.<br />
  </p>
