<div class="textblock" id="Oby">
  <p>
    <em>============================ Объявление:</em><br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Для начала составить таблицу конкурентного окружения:<br />
      - Таблица по конкурентам. По каким критериям что рекламируют (фразы, тексты). Как рекламируются.
      Все это, чтобы не пересекаться.<br />
      - <a href="http://spywords.ru/index.php?welcome">SpyWords</a> - инструмент выявления ключевых запросов конкурентов.<br />
      <br />

      <span class="v"><strong># Что писать?</span></strong><br />
      1) Тексты для людей - интересные, читабельные, ключевые слова.<br />
      2) Конкретные офферы.<br />
      3) Преимущества компании, а не товара - почемы у вас?<br />
      4) Цифры, латиница, аббревиатуры, бренд, логотип.<br />
      5) Выписать тезисы по всему тому, чего нет у конкурентов и те, которые подходят по критериям ваших клиентов.<br />
      <br />

      <span class="v"><strong># Принципы составления объявления</span></strong><br />
      1) Тип - цена & уникальное торговое предложение. В заголовке цена, в описании утп.<br />
      "Ремонт кухни от 12000. Выезд и смета в день обращения."<br /><br />
      2) Тип - сервис. В заголовке ключевое слово + сервис (дешево, за 1 день, срочно...).<br />
      "Снос зданий срочно."<br /><br />
      3) Тип - ODC (offer - deadline - call to action).<br />
      "Скидка 25% на установку вентиляции только до 25 сентября. Звоните!"<br />
      <br />

      <span class="v"><strong># Критерии эффективного объявления</span></strong><br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Вкусное.<br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Интересное.<br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Конкретное.
    </p>

<div class="alert alert-danger">
  <i class="fa fa-exclamation-triangle" aria-hidden="true"></i> Не должно быть никаких "жми" и "нажми" - не работает и приелось.
</div>

    <p>
      <span class="v"><strong># Расширения</strong></span><br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Быстрые ссылки (4 дополнительные ссылки к разным разделам сайты - доп. услуги);<br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Уточнения (будут показаны не все);<br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Контакты + адрес;<br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Структурированные описания.<br />
    </p>
</div>

