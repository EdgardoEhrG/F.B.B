<div class="textblock" id="Eta">
  <p>
    <em>============================ Этапы работы с Контекстной рекламой:</em><br />
      <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Прежде чем начинать рекламную кампанию, сайт должен пройти SEO.<br />
      <br />
      1) Выбор продуктов/услуг/категорий для продвижения.<br />
      2) Выбор рекламной системы (для начала).<br />
      3) Работа с семантикой (ключевые слова).
  </p>

<div class="alert alert-info">
  <i class="fa fa-info-circle" aria-hidden="true"></i> <strong>Методы сбора ключевых слов:</strong><br />
  - мозговой штурм.<br />
  - слова-синонимы и другие части речи.<br />
  - другой язык, сленг.<br />
  - номенклатура и категория.<br />
  - эхо.<br />
  - парсинг подсказок.<br />
  - смежные ниши (конверсия уменьшится).
</div>

<div class="alert alert-success">
  <i class="fa fa-circle" aria-hidden="true"></i> <strong>Ключевые слова, увеличивающие конверсию:</strong><br />
  подарок, бесплатно, скидка, дешево, доставка, от производителя, гарантия, распродажа.
</div>

