  <p>
    <span class="v"><strong># Этапы</strong></span><br />
    1) <strong>Исследование в соцсетях</strong> - что делают конкуренты в них, что публикуют, как часто, что интересно пользователям, а что нет.
    Что проходит хорошо, а что нет.<br />
    2) <strong>Генерация идей</strong> - определиться с рубриками (рубрики должны соответствовать имиджу компании и интересам аудитории).<br />
    3) <strong>Постановка целей и задач</strong> - не все соцсети подходят для каких-то задач (концепция SMART?).<br /><br />

    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Обратная связь.<br />
    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Повышение узнаваемости бренда / компании.<br />
    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Обучение клиентов.<br />
    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Повышение лояльности.<br />
    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Повышение охвата аудитории.<br />
    <i class="fa fa-thumb-tack rojo" aria-hidden="true"></i> Конвертация трафика.<br /><br />

    4)<strong>Структурирование и систематизация</strong> - когда публиковать и как часто? Анализ времени, когда публикуются
    посты конкурентов и какой отклик на них (умное копирование). Никакого массфолловинга и ботов! И без круглого времени.
    Можно использовать готовые данные аналитики от систем управления контентом. Оптимальный объем публикаций - не более 2-5
    постов в день / 2 дня (никакой надоедливости).
    Посты не должны хвалить компанию / бренд - больше информативности об услугах / товарах. Никаких продаж в соцсетях.<br />
    5) <strong>Тестирование</strong>.<br />
    6) <strong>Оптимизация</strong> - исправление ошибок (не менее чем через месяц после запуска).<br />
